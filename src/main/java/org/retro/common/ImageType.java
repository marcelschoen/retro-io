package org.retro.common;

/**
 * Lists all supported image types. The lowercase prefix part
 * of the name specifies the computer platform, the suffix
 * the disk image type suffix.
 */
public enum ImageType {
    dos_IMG,
    atarist_ST,
    atarist_MSA,
    amiga_FFS,
    amiga_OFS,
    c64_D64,
    c64_T64
}
